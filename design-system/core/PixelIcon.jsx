export const ICON_NAMES = Object.keys(ICONS);

/**
 * PixelIcon — a crisp 16×16 pixel icon, tinted via `color` (default currentColor).
 * Pass a known `name` (inline, preferred) or an explicit `src` (rendered as <img>).
 */
export function PixelIcon({
  name,
  src,
  base = 'assets/icons/',
  size = 16,
  color = 'currentColor',
  style,
  className = '',
  ...rest
}) {
  const data = name ? ICONS[name] : null;

  if (data) {
    const rects = data.split(';').map((run, i) => {
      const [x, y, w] = run.split(',');
      return React.createElement('rect', { key: i, x, y, width: w, height: 1 });
    });
    return (
      <svg
        className={`billd-pixel-icon ${className}`}
        viewBox="0 0 16 16"
        width={size}
        height={size}
        fill={color}
        shapeRendering="crispEdges"
        role="img"
        aria-label={name}
        style={{ display: 'inline-block', flex: '0 0 auto', ...style }}
        {...rest}
      >
        {rects}
      </svg>
    );
  }

  // Fallback: render an unknown/explicit icon from file as a crisp image.
  const url = src || (name ? `${base}${name}.svg` : undefined);
  return (
    <img
      className={`billd-pixel-icon ${className}`}
      src={url}
      width={size}
      height={size}
      alt={name || 'icon'}
      style={{ display: 'inline-block', imageRendering: 'pixelated', flex: '0 0 auto', ...style }}
      {...rest}
    />
  );
}
